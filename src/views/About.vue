      <h3>2. Limit Work in Process</h3>
      <p>By limiting how much unfinished work is in process, you can reduce the time it takes an item to travel through the Kanban system. You can also avoid problems caused by task switching and reduce the need to constantly re-prioritize items. You can learn more about the benefits of limiting WIP here.</p>
      <h3>3. Focus on Flow</h3>
      <p>By using work-in-process (WIP) limits and developing team-driven policies, you can optimize your Kanban system to improve the flow of work, collect metrics to analyze flow, and even get leading indicators of future problems by analyzing the flow of work. You can learn more about Kanban and Lean metrics here.</p>
      <h3>4. Continuous Improvement</h3>
      <p>Once your Kanban system is in place, it becomes the cornerstone for a culture of continuous improvement. Teams measure their effectiveness by tracking flow, quality, throughput, lead times and more. Experiments and analysis can change the system to improve the team’s effectiveness. You can learn more about continuous improvement here.</p>
